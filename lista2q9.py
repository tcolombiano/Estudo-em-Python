'''Dada a frase “Python é muito legal". use fatiamento para dar nome às variáveis contendo cada palavra.
Qual o tamanho dessa frase? E qual o tamanho de cada palavra?'''
palavra = 'Python é muito legal'
len('Python é muito legal')
print(len)
palavra1 = palavra[0:6]
palavra2 = palavra[6:9]
palavra3 = palavra[9:15]
palavra4 = palavra[15:20]
tamanho = len(palavra)
print('O tamanho da frase é:', tamanho)
len1 = len(palavra1)
len2 = len(palavra2)
len3 = len(palavra3)
len4 = len(palavra4)
print('A palavra 1 tem tamanho', len1)
print('A palavra 2 tem tamanho', len2)
print('A palavra 3 tem tamanho', len3)
print('A palavra 4 tem tamanho', len4)
